var operacao = {
    soma: function(numA, numB){
        return numA+numB
    },
    sub: function(numA, numB){
        return numA-numB
    },
    mult: function(numA, numB){
        return numA*numB
    },
    div: function(numA, numB){
        return numA/numB
    }
}

module.exports = operacao;